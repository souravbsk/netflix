const faqBtn = document.getElementById("faq");
const faqAns = document.getElementById("faq-ans");
faqBtn.addEventListener("click", () => {
	if (faqAns.className === "hidden") {
		faqAns.classList.remove("hidden");
	}
	else {
		faqAns.classList.add("hidden");
	}
})
